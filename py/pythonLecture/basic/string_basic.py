# 文字列(Stiring)

print("Hello World!")
print('1')

print("I'm file")
print('I"m file')

# 改行する場合
print("""
Hello world!!
How are you doing?
""")

print("Hello \nWorld!")
print("Hello \tWorld!")

# エスケープ
print("Hello \\nWorld!")
print('I\'m file')

# 文字の連結
print("Hello" + "World!")